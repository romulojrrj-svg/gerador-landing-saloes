  async function handleSubmit(event: FormEvent<HTMLFormElement>) {
    event.preventDefault();
    setSubmitErrorMessage("");
    logPerfEvent({
      route: "/salons/[id]/edit",
      step: "handleSubmit",
      id: initialSalon?.id,
      slug: initialSalon?.slug,
      imagesCount: realImages?.filter((image) => image.isReal).length ?? 0,
      servicesCount: selectedServices?.length ?? 0,
      source: repositoryStatus.activeSource,
    });
    await submitCurrentForm();
  }

  async function submitCurrentForm(overrides?: {
    copySuggestion?: SalonCopySuggestion;
    appliedCopy?: SalonCopySuggestion;
    copyHistory?: SalonCopySuggestion[];
    galleryImages?: SalonGalleryImage[];
    testimonials?: SalonTestimonial[];
  }) {
    const currentForm = formRef.current;

    if (!currentForm) {
      return;
    }

    const formData = new FormData(currentForm);
    const input = formDataToInput(formData);
    const orderedServices = buildOrderedServicesFromFormState(
      input.language,
      getFormFieldValue("observedServices"),
      selectedServices,
      serviceDescriptions,
    );
    const payloadSize = estimatePayloadSize({
      ...input,
      whatsappMessage: input.whatsappMessage,
      horizontalLogoUrl,
      galleryImages: overrides?.galleryImages ?? realImages,
      layoutImagePlan: layoutImagePlan,
      testimonials: overrides?.testimonials ?? realReviews,
      services: orderedServices,
    });
    const nextCopySuggestion = overrides?.copySuggestion ?? copySuggestion;
    const nextAppliedCopy = overrides?.appliedCopy ?? appliedCopy;
    const nextCopyHistory = overrides?.copyHistory ?? copyHistory;
    const nextLayoutImagePlan = buildEffectiveLayoutImagePlan(
      overrides?.galleryImages ?? realImages,
      layoutImagePlan,
    );
    let nextGalleryImages = syncImagesWithPlan(
      overrides?.galleryImages ?? realImages,
      nextLayoutImagePlan,
    );
    const nextTestimonials = overrides?.testimonials ?? realReviews;

    logPerfEvent({
      route: "/salons/[id]/edit",
      step: "buildPayload",
      id: initialSalon?.id,
      slug: initialSalon?.slug,
      payloadKb: payloadSize,
      imagesCount: (overrides?.galleryImages ?? realImages)?.filter((image) => image.isReal).length ?? 0,
      servicesCount: input.services?.length ?? 0,
      source: repositoryStatus.activeSource,
    });

    if (
      repositoryStatus.activeSource === "supabase" &&
      nextGalleryImages.some((image) => isInlineImageAsset(image.url) || isInlineImageAsset(image.src))
    ) {
      setIsPreparingSubmit(true);

      try {
        nextGalleryImages = await persistInlineGalleryImages({
          images: nextGalleryImages,
          salonName: input.name,
          salonSlug: initialSalon?.slug ?? input.name,
        });
        setRealImages(nextGalleryImages);
      } catch (error) {
        const nextError =
          error instanceof Error
            ? error.message
            : "Nao foi possivel enviar as novas fotos antes de salvar no Supabase.";
        setSubmitErrorMessage(nextError);
        setIsPreparingSubmit(false);
        return;
      }

      setIsPreparingSubmit(false);
    }

    await onSubmit({
      ...input,
      whatsappMessage: input.whatsappMessage,
      horizontalLogoUrl,
      selectedServices,
      services: orderedServices,
      galleryImages: nextGalleryImages,
      imageCandidates,
      imageSelectionSummary,
      layoutImagePlan: nextLayoutImagePlan,
      template,
      premiumEditorial,
      testimonials: nextTestimonials,
      sourceMaterials: buildSourceMaterials(
        nextGalleryImages,
        nextTestimonials,
      ),
      copySuggestions: nextCopySuggestion,
      generatedCopy: nextAppliedCopy,
      copyHistory: nextCopyHistory,
      lastGeneratedAt: nextCopySuggestion?.generatedAt,
      lastAppliedAt: nextAppliedCopy?.appliedAt,
    });
  }

  async function handleReviewScreenshotUpload(files: File[]) {
    const allowedTypes = ["image/jpeg", "image/png", "image/webp"];
    const maxBytes = 5 * 1024 * 1024;

    if (!files.length) {
      return [];
    }

    if (files.length > 10) {
      throw new Error("Selecione ate 10 prints por vez.");
    }

    if (files.some((file) => !allowedTypes.includes(file.type))) {
      throw new Error("Envie prints em jpg, jpeg, png ou webp.");
    }

    if (files.some((file) => file.size > maxBytes)) {
      throw new Error("Um ou mais prints excedem o limite de 5 MB.");
    }

    const createdImages: SalonGalleryImage[] = [];

    for (const file of files) {
      const dataUrl = await compressImageToDataUrl(file);
      const imageId = createLocalId("review-screenshot");
      createdImages.push({
        id: imageId,
        url: dataUrl,
        src: dataUrl,
        alt: `${initialSalon?.name || "Salao"} - feedback de paciente`,
        title: file.name,
        type: "gallery",
        source: "manual",
        isReal: true,
        selectedForLanding: false,
        createdAt: new Date().toISOString(),
      });
    }

    setRealImages((current) => [...current, ...createdImages]);
    return createdImages;
  }

